        //    Chuỗi Đạt Chuẩn Dạng 2.
        //• Không có khoảng trắng ở đầu và cuối chuỗi
        //• Giữa các từ trong chuỗi chỉ tồn tại một khoảng trắng duy nhất
        //• Ký tự đầu tiên trong chuỗi phải là ký tự in hoa,
        //• Hết câu bằng dấu chấm
        //• Các ký tự đầu tiên ở mỗi từ phải viết được viết hoa
    private void btChuanHoaDang2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btChuanHoaDang2ActionPerformed
        // TODO add your handling code here:
        if(strInput == null){
            JOptionPane.showMessageDialog(null, "Please fill a string.", "Error", JOptionPane.ERROR_MESSAGE);
            btToString.requestFocus();
            return;
        } else {
            //   khong   co khoang  trang   .    
            strInput = strInput.trim();
            strInput = strInput.replaceAll("\\s+", " ");
            strInput = strInput.replaceAll("\\s\\.", ".");
            if(strInput.charAt(strInput.length() - 1) != '.') strInput = strInput + ".";
            String arrStr[] = strInput.split("\\s+");
            
            StringBuilder strB = new StringBuilder();
            
            
            for(int i = 0; i < arrStr.length; i++){
                arrStr[i] = arrStr[i].substring(0,1).toUpperCase() + arrStr[i].substring(1).toLowerCase();
                strB.append(arrStr[i] + " ");
            }
            strInput = strB.toString();
            strInput.trim();
            taOutput.setText(strInput);
        }
    }//GEN-LAST:event_btChuanHoaDang2ActionPerformed

