    private void btChuanHoaDang1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btChuanHoaDang1ActionPerformed
        // TODO add your handling code here:
        if(strInput == null){
            JOptionPane.showMessageDialog(null, "Please fill a string.", "Error", JOptionPane.ERROR_MESSAGE);
            btToString.requestFocus();
            return;
        } else {
            strInput = strInput.trim();
            strInput = strInput.toLowerCase();
            strInput = strInput.substring(0,1).toUpperCase() + strInput.substring(1).replaceAll("\\s+", " ");
            strInput = strInput.replaceAll("\\s+\\.", ".");
            taOutput.setText(strInput);
        }
    }//GEN-LAST:event_btChuanHoaDang1ActionPerformed

