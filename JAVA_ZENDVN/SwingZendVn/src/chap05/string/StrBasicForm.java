    private void btToNumberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btToNumberActionPerformed
        // TODO add your handling code here:
        double temp = 0;
        try{
            switch(str){
                case "Str 1":
                    temp = Double.parseDouble(str1);
                    break;
                case "Str 2":
                    temp = Double.parseDouble(str2);
                    break;
                case "Str 3":
                    temp = Double.parseDouble(str3);
                    break;
            }
        } catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Wrong", "Message", JOptionPane.ERROR_MESSAGE);
            tfToNumber.requestFocus();
            return;
        }
        tfToNumber.setText(Double.toString(temp));
    }//GEN-LAST:event_btToNumberActionPerformed

    private void btCompareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCompareActionPerformed
        // TODO add your handling code here:
        if(str1 == null || str2 == null){
            JOptionPane.showMessageDialog(null, "You have to fill subs string first.");
            btToStr1.requestFocus();
            return;
        } else{
            if(str1.equals(str2)){
                JOptionPane.showMessageDialog(null, "Str1 equals to str2");
            } else{
                if(str1.equalsIgnoreCase(str2)){
                    JOptionPane.showMessageDialog(null,"Str1 equals ignore case to str2");
                } else{
                    JOptionPane.showMessageDialog(null, "Str1 not equals to str2");
                }
            }
        }
    }//GEN-LAST:event_btCompareActionPerformed

