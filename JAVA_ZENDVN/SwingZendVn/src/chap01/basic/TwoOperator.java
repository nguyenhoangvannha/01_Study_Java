    private void bMaxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bMaxActionPerformed
        // TODO add your handling code here:
        
        getNumber();
        
        tfMax.setText(Integer.toString(Math.max(number1, number2)));
    }//GEN-LAST:event_bMaxActionPerformed

    private void bRandomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRandomActionPerformed
        // TODO add your handling code here:
        
        getNumber();
        int max = Math.max(number1, number2);
        int min = Math.min(number1, number2);
        int random = (int)(Math.random()*(max - min + 1)) + min;
        tfRandom.setText(Integer.toString(random));
    }//GEN-LAST:event_bRandomActionPerformed

