    private void getNumber(){
        try{
            number1 = Integer.parseInt(tfNumber1.getText());
        } catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error: " + e.toString(),"Caution",JOptionPane.ERROR_MESSAGE);
            tfNumber1.requestFocus();
            tfResult.setText("");
            tfMax.setText("");
            tfRandom.setText("");
            return;
        }
        try{
            number2 = Integer.parseInt(tfNumber2.getText());
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error: " + e.toString(), "Caution", JOptionPane.ERROR_MESSAGE);
            tfNumber2.requestFocus();
            tfResult.setText("");
            tfMax.setText("");
            tfRandom.setText("");
            return;
        }
    }
    private void bExeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bExeActionPerformed
        // TODO add your handling code here:
        
        
        getNumber();
        int result = 0;
        switch(operator){
            case "Addition": result = number1 + number2;
                break;
            case "Subtract": result = number1 - number2;
                break;
            case "Multiply": result = number1 * number2;
                break;
            case "Divide": result = (int)number1/number2;
                break;
        }
        tfResult.setText(Integer.toString(result));
    }//GEN-LAST:event_bExeActionPerformed

